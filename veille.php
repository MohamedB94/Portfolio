<h2>QU'EST-CE QUE LA VEILLE TECHNOLOGIQUE ?</h2>
<p>La veille technologique, élément de la veille stratégique, consiste à surveiller les évolutions techniques, les innovations dans un <br>secteur d'activité donnée. La veille technologique comprend notamment la surveillance, la collecte, le partage et la diffusion <br> d'information permettant d'anticiper ou de s'informer sur des changements en matière de recherche, développement, brevet <br>, lancement de nouveaux produits, matériaux, processus, concepts, innovation de fabrication, etc…. Cela a pour but d'évaluer l'impact <br> sur l'environnement et l'organisation.</p>
<h2>SUJET DE LA VEILLE TECHNOLOGIQUE</h2>
<p>Pour ma veille technologique, j'ai décidé de traiter le sujet « des données personnelles » .La thématique est la suivante : <br> Quelles mesures de sécurité sont mises en place pour garantir la protection des données personnelles dans les <br>applications ?</p>
